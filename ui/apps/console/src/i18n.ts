export type TranslationKey = keyof typeof consoleMessages
export type Translate = (key: TranslationKey, params?: Record<string, unknown>) => string

const data = {
  en: {console: consoleMessages},
  ru: {console: ruConsoleMessages},
}

export function isAppLanguage(value: unknown): value is AppLanguage {
  return value === 'en' || value === 'ru'
}

export function createTranslator(language: AppLanguage): Translate {
  const i18n = new I18N({
    data,
    fallbackLang: fallbackLanguage,
    lang: language,
  })
  const keyset = i18n.keyset<TranslationKey>('console')

  return (key, params) => keyset(key, params)
}
