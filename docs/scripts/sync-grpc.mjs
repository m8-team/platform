import {execFile as execFileCallback} from 'node:child_process';
import fs from 'node:fs/promises';
import os from 'node:os';
import path from 'node:path';
import {promisify} from 'node:util';
import {fileURLToPath} from 'node:url';

const execFile = promisify(execFileCallback);
const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const docsRoot = path.resolve(__dirname, '..');
const repoRoot = path.resolve(docsRoot, '..');
const generatedFileHeader = '<!-- Generated by docs/scripts/sync-grpc.mjs -->';
const serviceDescriptionOption = '[m8.platform.common.extension.v1.description]';
const operationOption = '[gnostic.openapi.v3.operation]';
const operationInfoOption = '[google.longrunning.operation_info]';

const groupConfigs = new Map([
  [
    'iam',
    {
      docsSegments: ['iam'],
      title: 'IAM',
      intro: 'Этот gRPC reference организован по сервисам `IAM`.',
    },
  ],
  [
    'resourcemanager',
    {
      docsSegments: ['resource-manager'],
      title: 'Resource Manager',
      intro: 'Этот gRPC reference организован по сервисам `Resource Manager`.',
    },
  ],
]);

const scalarTypes = new Map([
  ['TYPE_DOUBLE', 'double'],
  ['TYPE_FLOAT', 'float'],
  ['TYPE_INT64', 'int64'],
  ['TYPE_UINT64', 'uint64'],
  ['TYPE_INT32', 'int32'],
  ['TYPE_FIXED64', 'fixed64'],
  ['TYPE_FIXED32', 'fixed32'],
  ['TYPE_BOOL', 'bool'],
  ['TYPE_STRING', 'string'],
  ['TYPE_BYTES', 'bytes'],
  ['TYPE_UINT32', 'uint32'],
  ['TYPE_SFIXED32', 'sfixed32'],
  ['TYPE_SFIXED64', 'sfixed64'],
  ['TYPE_SINT32', 'sint32'],
  ['TYPE_SINT64', 'sint64'],
]);

async function main() {
  const image = await buildImage();
  const registry = buildRegistry(image);
  const services = collectServices(image, registry);

  await removeGeneratedGrpcRoots(services);
  await writeGroupPages(services);

  console.log('Generated gRPC docs into docs/services/*/api-reference/grpc.');
}

async function buildImage() {
  const tempDir = await fs.mkdtemp(path.join(os.tmpdir(), 'm8-grpc-doc-'));
  const imagePath = path.join(tempDir, 'image.json');

  try {
    await execFile(
      'buf',
      ['build', '--exclude-imports', '-o', `${imagePath}#format=json`],
      {cwd: repoRoot},
    );

    return JSON.parse(await fs.readFile(imagePath, 'utf8'));
  } finally {
    await fs.rm(tempDir, {recursive: true, force: true});
  }
}

function buildRegistry(image) {
  const registry = {
    messages: new Map(),
    enums: new Map(),
  };

  for (const file of image.file ?? []) {
    registerMessages(registry, file, file.messageType ?? [], `.${file.package}`, [4]);
    registerEnums(registry, file, file.enumType ?? [], `.${file.package}`, [5]);
  }

  return registry;
}

function registerMessages(registry, file, messages, parentFullName, parentPath) {
  for (const [index, message] of messages.entries()) {
    const fullName = `${parentFullName}.${message.name}`;
    const descriptorPath = [...parentPath, index];

    registry.messages.set(fullName, {
      file,
      descriptor: message,
      fullName,
      descriptorPath,
      comment: sourceComment(file, descriptorPath),
    });

    registerMessages(
      registry,
      file,
      message.nestedType ?? [],
      fullName,
      [...descriptorPath, 3],
    );
    registerEnums(
      registry,
      file,
      message.enumType ?? [],
      fullName,
      [...descriptorPath, 4],
    );
  }
}

function registerEnums(registry, file, enums, parentFullName, parentPath) {
  for (const [index, enumType] of enums.entries()) {
    const fullName = `${parentFullName}.${enumType.name}`;
    const descriptorPath = [...parentPath, index];

    registry.enums.set(fullName, {
      file,
      descriptor: enumType,
      fullName,
      descriptorPath,
      comment: sourceComment(file, descriptorPath),
    });
  }
}

function collectServices(image, registry) {
  const services = [];

  for (const file of image.file ?? []) {
    for (const [serviceIndex, service] of (file.service ?? []).entries()) {
      const relativeDir = path.dirname(file.name);
      const groupSegments = inferServiceGroupSegments(relativeDir);
      const groupConfig = resolveGroupConfig(groupSegments);
      const displayName = humanizeServiceName(service.name);
      const navName = toProductName(displayName);
      const description =
        readString(service.options?.[serviceDescriptionOption]) ??
        sourceComment(file, [6, serviceIndex]) ??
        `${displayName} gRPC API.`;

      services.push({
        file,
        service,
        registry,
        groupKey: groupSegments.join('/'),
        groupSegments,
        groupConfig,
        serviceIndex,
        serviceName: service.name,
        navName,
        slug: toKebabCase(navName),
        description,
        methods: collectMethods({file, service, serviceIndex}),
      });
    }
  }

  return services.sort((left, right) => {
    const byGroup = left.groupConfig.title.localeCompare(right.groupConfig.title);

    if (byGroup !== 0) {
      return byGroup;
    }

    return left.navName.localeCompare(right.navName);
  });
}

function collectMethods({file, service, serviceIndex}) {
  return (service.method ?? []).map((method, methodIndex) => {
    const operation = method.options?.[operationOption];
    const description =
      readString(operation?.description) ??
      sourceComment(file, [6, serviceIndex, 2, methodIndex]) ??
      'No description.';

    return {
      method,
      methodIndex,
      name: method.name,
      slug: toKebabCase(method.name),
      description,
      summary: readString(operation?.summary),
      operationId: readString(operation?.operationId),
    };
  });
}

async function removeGeneratedGrpcRoots(services) {
  const roots = new Set(
    services.map((service) =>
      path.join(docsRoot, 'services', ...service.groupConfig.docsSegments, 'api-reference', 'grpc'),
    ),
  );

  for (const root of roots) {
    await fs.rm(root, {recursive: true, force: true});
  }
}

async function writeGroupPages(services) {
  const servicesByGroup = groupBy(services, (service) => service.groupKey);

  for (const [groupKey, groupServices] of servicesByGroup.entries()) {
    const groupConfig = groupServices[0].groupConfig;
    const groupRoot = path.join(
      docsRoot,
      'services',
      ...groupConfig.docsSegments,
      'api-reference',
      'grpc',
    );

    await fs.mkdir(groupRoot, {recursive: true});
    await fs.writeFile(
      path.join(groupRoot, 'index.md'),
      renderGroupOverview({groupConfig, services: groupServices}),
      'utf8',
    );

    for (const service of groupServices) {
      await writeServicePages({groupRoot, service});
    }
  }
}

async function writeServicePages({groupRoot, service}) {
  const serviceRoot = path.join(groupRoot, service.slug);

  await fs.mkdir(serviceRoot, {recursive: true});
  await fs.writeFile(path.join(serviceRoot, 'index.md'), renderServiceOverview(service), 'utf8');
  await fs.writeFile(path.join(serviceRoot, 'toc.yaml'), renderServiceToc(service), 'utf8');

  for (const method of service.methods) {
    await fs.writeFile(
      path.join(serviceRoot, `${method.slug}.md`),
      renderMethodPage({service, method}),
      'utf8',
    );
  }
}

function renderGroupOverview({groupConfig, services}) {
  const lines = [
    generatedFileHeader,
    '',
    `# ${groupConfig.title} API: gRPC reference`,
    '',
    groupConfig.intro,
    '',
    '## Services',
    '',
    '| Service | Description |',
    '| --- | --- |',
  ];

  for (const service of services) {
    lines.push(
      `| [${service.navName}](./${service.slug}/index.md) | ${escapeTableCell(service.description)} |`,
    );
  }

  return `${lines.join('\n')}\n`;
}

function renderServiceOverview(service) {
  const lines = [
    generatedFileHeader,
    '',
    `# ${service.groupConfig.title} API, gRPC: ${service.serviceName}`,
    '',
    service.description,
    '',
    '## Methods',
    '',
    '| Method | Description |',
    '| --- | --- |',
  ];

  for (const method of service.methods) {
    lines.push(
      `| [${method.name}](./${method.slug}.md) | ${escapeTableCell(method.description)} |`,
    );
  }

  return `${lines.join('\n')}\n`;
}

function renderServiceToc(service) {
  const lines = [
    `# ${generatedFileHeader.replace(/^<!-- | -->$/g, '')}`,
    'items:',
    '  - name: Overview',
    '    href: index.md',
  ];

  for (const method of service.methods) {
    lines.push(`  - name: ${method.name}`, `    href: ${method.slug}.md`);
  }

  return `${lines.join('\n')}\n`;
}

function renderMethodPage({service, method}) {
  const requestType = normalizeFullName(method.method.inputType);
  const responseType = normalizeFullName(method.method.outputType);
  const relatedTypes = collectRelatedTypes({
    method: method.method,
    packageName: service.file.package,
    registry: service.registry,
  });
  const lines = [
    generatedFileHeader,
    '',
    `# ${service.groupConfig.title} API, gRPC: ${service.serviceName}.${method.name}`,
    '',
    method.description,
    '',
    '## gRPC request',
    '',
    '```proto',
    `rpc ${method.name} (${shortTypeName(requestType)}) returns (${shortTypeName(responseType)})`,
    '```',
  ];

  const operationInfo = method.method.options?.[operationInfoOption];

  if (operationInfo) {
    lines.push(
      '',
      'The method starts a long-running operation.',
      '',
      `- Response type: \`${operationInfo.responseType}\``,
      `- Metadata type: \`${operationInfo.metadataType}\``,
    );
  }

  for (const typeName of relatedTypes) {
    const section = renderTypeSection({typeName, registry: service.registry});

    if (section) {
      lines.push('', section);
    }
  }

  return `${lines.join('\n')}\n`;
}

function collectRelatedTypes({method, packageName, registry}) {
  const ordered = [];
  const visited = new Set();
  const queue = [];

  enqueue(method.inputType);
  enqueue(method.outputType);

  const operationInfo = method.options?.[operationInfoOption];

  if (operationInfo?.responseType) {
    enqueue(resolveOperationInfoType({packageName, typeName: operationInfo.responseType, registry}));
  }

  if (operationInfo?.metadataType) {
    enqueue(resolveOperationInfoType({packageName, typeName: operationInfo.metadataType, registry}));
  }

  while (queue.length > 0) {
    const typeName = queue.shift();

    if (visited.has(typeName)) {
      continue;
    }

    visited.add(typeName);
    ordered.push(typeName);

    const message = registry.messages.get(typeName);

    if (!message || message.descriptor.options?.mapEntry) {
      continue;
    }

    for (const field of message.descriptor.field ?? []) {
      const fieldMapEntry = getMapEntry(field, registry);

      if (fieldMapEntry) {
        const valueField = fieldMapEntry.descriptor.field?.find((candidate) => candidate.name === 'value');

        if (valueField) {
          enqueueFieldType(valueField);
        }

        continue;
      }

      enqueueFieldType(field);
    }
  }

  return ordered;

  function enqueueFieldType(field) {
    if (field.type === 'TYPE_MESSAGE' && field.typeName) {
      enqueue(field.typeName);
      return;
    }

    if (field.type === 'TYPE_ENUM' && field.typeName) {
      enqueue(field.typeName);
    }
  }

  function enqueue(typeName) {
    const normalizedTypeName = normalizeFullName(typeName);

    if (!visited.has(normalizedTypeName)) {
      queue.push(normalizedTypeName);
    }
  }
}

function renderTypeSection({typeName, registry}) {
  const message = registry.messages.get(typeName);

  if (message && !message.descriptor.options?.mapEntry) {
    return renderMessageSection({message, registry});
  }

  const enumType = registry.enums.get(typeName);

  if (enumType) {
    return renderEnumSection(enumType);
  }

  return renderExternalTypeSection(typeName);
}

function renderMessageSection({message, registry}) {
  const lines = [`## ${shortTypeName(message.fullName)}`];

  if (message.comment) {
    lines.push('', message.comment);
  }

  lines.push('', '```json', JSON.stringify(renderMessageExample(message, registry), null, 2), '```');

  const fields = message.descriptor.field ?? [];

  if (fields.length > 0) {
    lines.push('', '| Field | Type | Description |', '| --- | --- | --- |');

    for (const [fieldIndex, field] of fields.entries()) {
      const fieldName = field.label === 'LABEL_REPEATED' && !getMapEntry(field, registry)
        ? `${field.name}[]`
        : field.name;
      const fieldComment =
        sourceComment(message.file, [...message.descriptorPath, 2, fieldIndex]) ?? 'No description.';
      const enumDescription = enumValuesDescription(field, registry);
      const description = enumDescription
        ? `${fieldComment}\n\n${enumDescription}`
        : fieldComment;

      lines.push(
        `| ${fieldName} | ${escapeTableCell(formatFieldType(field, registry))} | ${escapeTableCell(description)} |`,
      );
    }
  }

  return lines.join('\n');
}

function renderEnumSection(enumType) {
  const lines = [`## ${shortTypeName(enumType.fullName)}`];

  if (enumType.comment) {
    lines.push('', enumType.comment);
  }

  lines.push('', '| Value | Number | Description |', '| --- | ---: | --- |');

  for (const [valueIndex, value] of (enumType.descriptor.value ?? []).entries()) {
    const description =
      sourceComment(enumType.file, [...enumType.descriptorPath, 2, valueIndex]) ?? 'No description.';

    lines.push(`| ${value.name} | ${value.number} | ${escapeTableCell(description)} |`);
  }

  return lines.join('\n');
}

function renderExternalTypeSection(typeName) {
  if (typeName === '.google.protobuf.Empty') {
    return ['## google.protobuf.Empty', '', '```json', '{}', '```'].join('\n');
  }

  if (typeName === '.google.longrunning.Operation') {
    return [
      '## google.longrunning.Operation',
      '',
      'Long-running operation returned by asynchronous API methods.',
      '',
      '```json',
      JSON.stringify(
        {
          name: 'string',
          metadata: {},
          done: true,
          error: {},
          response: {},
        },
        null,
        2,
      ),
      '```',
    ].join('\n');
  }

  return undefined;
}

function renderMessageExample(message, registry, seen = new Set()) {
  if (seen.has(message.fullName)) {
    return {};
  }

  seen.add(message.fullName);

  const result = {};

  for (const field of message.descriptor.field ?? []) {
    result[field.name] = sampleForField(field, registry, seen);
  }

  seen.delete(message.fullName);

  return result;
}

function sampleForField(field, registry, seen) {
  const mapEntry = getMapEntry(field, registry);

  if (mapEntry) {
    const valueField = mapEntry.descriptor.field?.find((candidate) => candidate.name === 'value');

    return {
      key: valueField ? sampleForScalarOrType(valueField, registry, seen) : 'string',
    };
  }

  const value = sampleForScalarOrType(field, registry, seen);

  if (field.label === 'LABEL_REPEATED') {
    return [value];
  }

  return value;
}

function sampleForScalarOrType(field, registry, seen) {
  switch (field.type) {
    case 'TYPE_DOUBLE':
    case 'TYPE_FLOAT':
      return 0.0;
    case 'TYPE_INT64':
    case 'TYPE_UINT64':
    case 'TYPE_INT32':
    case 'TYPE_FIXED64':
    case 'TYPE_FIXED32':
    case 'TYPE_UINT32':
    case 'TYPE_SFIXED32':
    case 'TYPE_SFIXED64':
    case 'TYPE_SINT32':
    case 'TYPE_SINT64':
      return 0;
    case 'TYPE_BOOL':
      return true;
    case 'TYPE_BYTES':
    case 'TYPE_STRING':
      return 'string';
    case 'TYPE_ENUM':
      return shortTypeName(field.typeName);
    case 'TYPE_MESSAGE':
      return sampleForMessageType(field.typeName, registry, seen);
    default:
      return 'string';
  }
}

function sampleForMessageType(typeName, registry, seen) {
  const normalizedTypeName = normalizeFullName(typeName);

  if (normalizedTypeName === '.google.protobuf.Timestamp') {
    return 'string';
  }

  if (normalizedTypeName === '.google.protobuf.FieldMask') {
    return 'string';
  }

  if (normalizedTypeName === '.google.protobuf.Duration') {
    return 'string';
  }

  if (normalizedTypeName === '.google.protobuf.Empty') {
    return {};
  }

  const message = registry.messages.get(normalizedTypeName);

  if (!message || message.descriptor.options?.mapEntry) {
    return {};
  }

  return renderMessageExample(message, registry, seen);
}

function formatFieldType(field, registry) {
  const mapEntry = getMapEntry(field, registry);

  if (mapEntry) {
    const keyField = mapEntry.descriptor.field?.find((candidate) => candidate.name === 'key');
    const valueField = mapEntry.descriptor.field?.find((candidate) => candidate.name === 'value');
    const keyType = keyField ? formatFieldType(keyField, registry) : 'string';
    const valueType = valueField ? formatFieldType(valueField, registry) : 'string';

    return `map<${keyType}, ${valueType}>`;
  }

  if (field.type === 'TYPE_ENUM') {
    return `enum ${shortTypeName(field.typeName)}`;
  }

  if (field.type === 'TYPE_MESSAGE') {
    return shortTypeName(field.typeName);
  }

  return scalarTypes.get(field.type) ?? field.type;
}

function enumValuesDescription(field, registry) {
  if (field.type !== 'TYPE_ENUM' || !field.typeName) {
    return undefined;
  }

  const enumType = registry.enums.get(normalizeFullName(field.typeName));

  if (!enumType) {
    return undefined;
  }

  const values = (enumType.descriptor.value ?? []).map((value) => `\`${value.name}\``);

  if (values.length === 0) {
    return undefined;
  }

  return `Available values: ${values.join(', ')}.`;
}

function getMapEntry(field, registry) {
  if (field.label !== 'LABEL_REPEATED' || field.type !== 'TYPE_MESSAGE' || !field.typeName) {
    return undefined;
  }

  const message = registry.messages.get(normalizeFullName(field.typeName));

  return message?.descriptor.options?.mapEntry ? message : undefined;
}

function resolveOperationInfoType({packageName, typeName, registry}) {
  const normalizedTypeName = normalizeFullName(typeName);

  if (registry.messages.has(normalizedTypeName)) {
    return normalizedTypeName;
  }

  const packageRelativeTypeName = `.${packageName}.${typeName}`;

  if (registry.messages.has(packageRelativeTypeName)) {
    return packageRelativeTypeName;
  }

  return packageRelativeTypeName;
}

function sourceComment(file, descriptorPath) {
  const location = (file.sourceCodeInfo?.location ?? []).find((candidate) =>
    arraysEqual(candidate.path ?? [], descriptorPath),
  );

  return normalizeComment(location?.leadingComments ?? location?.trailingComments);
}

function normalizeComment(value) {
  if (typeof value !== 'string') {
    return undefined;
  }

  const normalized = value
    .split('\n')
    .map((line) => line.trim())
    .join('\n')
    .replace(/\n{3,}/g, '\n\n')
    .trim();

  return normalized.length > 0 ? normalized : undefined;
}

function inferServiceGroupSegments(relativeDir) {
  const segments = relativeDir.split(path.sep).filter(Boolean);
  const normalizedSegments =
    segments[0] === 'm8' && segments[1] === 'platform' ? segments.slice(2) : [...segments];

  if (normalizedSegments.length > 0 && /^v\d/.test(normalizedSegments[normalizedSegments.length - 1])) {
    normalizedSegments.pop();
  }

  return normalizedSegments;
}

function resolveGroupConfig(groupSegments) {
  const groupKey = groupSegments.join('/');
  const configured = groupConfigs.get(groupKey);

  if (configured) {
    return configured;
  }

  const title = groupSegments.map(humanizeServiceName).join(' / ');

  return {
    docsSegments: groupSegments.map(toKebabCase),
    title,
    intro: `Этот gRPC reference организован по сервисам \`${title}\`.`,
  };
}

function groupBy(values, keyForValue) {
  const groups = new Map();

  for (const value of values) {
    const key = keyForValue(value);
    const group = groups.get(key) ?? [];

    group.push(value);
    groups.set(key, group);
  }

  return groups;
}

function normalizeFullName(value) {
  if (typeof value !== 'string' || value.length === 0) {
    return '';
  }

  return value.startsWith('.') ? value : `.${value}`;
}

function shortTypeName(value) {
  return normalizeFullName(value).replace(/^\./, '').split('.').pop();
}

function humanizeServiceName(value) {
  return value
    .replace(/([a-z0-9])([A-Z])/g, '$1 $2')
    .replace(/([A-Z])([A-Z][a-z])/g, '$1 $2')
    .trim();
}

function toProductName(displayName) {
  return displayName.replace(/\s+Service$/, '').trim();
}

function toKebabCase(value) {
  return humanizeServiceName(value)
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/^-+|-+$/g, '');
}

function readString(value) {
  return typeof value === 'string' && value.trim().length > 0 ? value.trim() : undefined;
}

function escapeTableCell(value) {
  return String(value)
    .replace(/\|/g, '\\|')
    .replace(/\n{2,}/g, '<br/><br/>')
    .replace(/\n/g, '<br/>');
}

function arraysEqual(left, right) {
  return left.length === right.length && left.every((value, index) => value === right[index]);
}

await main();
