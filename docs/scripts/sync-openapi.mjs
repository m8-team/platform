import fs from 'node:fs/promises';
import path from 'node:path';
import {fileURLToPath} from 'node:url';

import yaml from 'js-yaml';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const docsRoot = path.resolve(__dirname, '..');
const repoRoot = path.resolve(docsRoot, '..');
const sourceOpenapiRoot = path.join(repoRoot, 'api', 'generate', 'openapi');
const sourceProtoRoot = path.join(repoRoot, 'api', 'proto');
const generatedRoot = path.join(docsRoot, 'generated');
const generatedServicesRoot = path.join(generatedRoot, 'services');

async function main() {
  await fs.rm(generatedRoot, {recursive: true, force: true});
  await fs.mkdir(generatedServicesRoot, {recursive: true});

  const specFiles = (await walk(sourceOpenapiRoot))
    .filter((filePath) => filePath.endsWith('.openapi.yaml'))
    .sort();

  const services = [];

  for (const specFile of specFiles) {
    const source = yaml.load(await fs.readFile(specFile, 'utf8'));

    if (!isObject(source) || !isObject(source.paths) || Object.keys(source.paths).length === 0) {
      continue;
    }

    const relativeDir = path.relative(sourceOpenapiRoot, path.dirname(specFile));
    const protoFile = path.join(
      sourceProtoRoot,
      relativeDir,
      path.basename(specFile).replace(/\.openapi\.yaml$/, '.proto'),
    );
    const protoMeta = await readProtoMetadata(protoFile);
    const serviceName = protoMeta.serviceName ?? inferServiceNameFromSpec(source, specFile);
    const slug = toKebabCase(serviceName);
    const displayName = humanizeServiceName(serviceName);
    const description =
      protoMeta.description ??
      readString(source.info?.description) ??
      `${displayName} HTTP API.`;
    const version = readString(source.info?.version) ?? inferVersion(relativeDir);
    const basePath = inferBasePath(Object.keys(source.paths));
    const tags = collectTags(source);
    const serviceDir = path.join(generatedServicesRoot, slug);

    source.info = {
      ...(isObject(source.info) ? source.info : {}),
      title: `${displayName} API`,
      description,
      version,
    };

    await fs.mkdir(serviceDir, {recursive: true});
    await fs.writeFile(
      path.join(serviceDir, 'openapi.yaml'),
      yaml.dump(source, {lineWidth: -1, noRefs: true}),
      'utf8',
    );
    await fs.writeFile(
      path.join(serviceDir, 'overview.md'),
      renderServiceOverview({
        displayName,
        description,
        version,
        basePath,
        protoFile: toRepoPath(protoFile),
        openapiFile: toRepoPath(specFile),
      }),
      'utf8',
    );

    services.push({
      slug,
      displayName,
      description,
      serviceName,
      version,
      basePath,
      tags,
    });
  }

  services.sort((left, right) => left.displayName.localeCompare(right.displayName, 'en'));

  await fs.writeFile(path.join(docsRoot, 'api-reference.md'), renderApiReferencePage(services), 'utf8');
  await fs.writeFile(path.join(docsRoot, 'toc.yaml'), yaml.dump(renderToc(services), {lineWidth: -1}), 'utf8');

  console.log(`Generated Diplodoc sources for ${services.length} service(s).`);
}

function renderToc(services) {
  return {
    title: 'M8 Platform Docs',
    href: 'index.md',
    items: [
      {
        name: 'API Reference',
        href: 'api-reference.md',
        items: services.map((service) => {
          const includePath = path.posix.join('generated', 'services', service.slug);

          return {
            name: service.displayName,
            include: {
              path: includePath,
              includers: [
                {
                  name: 'openapi',
                  input: path.posix.join(includePath, 'openapi.yaml'),
                  leadingPage: {
                    spec: {
                      renderMode: 'hidden',
                    },
                  },
                  tags: renderTagOverrides(service),
                },
              ],
              mode: 'link',
            },
          };
        }),
      },
    ],
  };
}

function renderTagOverrides(service) {
  const overrides = {
    __root__: {
      name: 'Обзор',
      path: 'overview.md',
      alias: 'overview',
    },
  };

  if (service.tags.length === 1 && service.tags[0] === service.serviceName) {
    overrides[service.serviceName] = {
      name: 'Операции',
      alias: 'operations',
    };
  }

  return overrides;
}

function renderApiReferencePage(services) {
  const lines = [
    '<!-- Generated by docs/scripts/sync-openapi.mjs -->',
    '',
    '# API Reference',
    '',
    'Справочник REST API сервисов платформы, собранный в Diplodoc из OpenAPI-спецификаций, которые генерируются через `buf generate`.',
    '',
    'В публикацию попадают только сервисы с HTTP-методами и непустым `paths`.',
    '',
    '## Сервисы',
    '',
    '| Сервис | Версия | Базовый путь | Описание |',
    '| --- | --- | --- | --- |',
  ];

  if (services.length === 0) {
    lines.push('| Нет опубликованных сервисов | - | - | После генерации OpenAPI здесь появятся разделы сервисов. |');
  } else {
    for (const service of services) {
      lines.push(
        `| ${service.displayName} | \`${service.version}\` | \`${service.basePath}\` | ${escapeTableCell(service.description)} |`,
      );
    }
  }

  return `${lines.join('\n')}\n`;
}

function renderServiceOverview({displayName, description, version, basePath, protoFile, openapiFile}) {
  return `<!-- Generated by docs/scripts/sync-openapi.mjs -->

# ${displayName}

${description}

## Кратко

- Версия: \`${version}\`
- Базовый путь: \`${basePath}\`
- Исходный protobuf: \`${protoFile}\`
- Исходный OpenAPI: \`${openapiFile}\`

## Примечание

Этот раздел собран автоматически из OpenAPI-спецификации. Ниже опубликованы операции и схемы текущего сервиса.
`;
}

async function readProtoMetadata(protoFile) {
  try {
    const source = await fs.readFile(protoFile, 'utf8');
    const match = source.match(/((?:\/\/[^\n]*\n)*)service\s+([A-Za-z0-9_]+)\s*\{/m);

    if (!match) {
      return {};
    }

    const comment = match[1]
      .split('\n')
      .map((line) => line.replace(/^\/\/\s?/, '').trim())
      .filter(Boolean)
      .join(' ')
      .trim();

    return {
      serviceName: match[2],
      description: comment || undefined,
    };
  } catch {
    return {};
  }
}

function inferServiceNameFromSpec(spec, specFile) {
  const firstTag = collectTags(spec)[0];

  if (firstTag) {
    return firstTag;
  }

  return path.basename(specFile).replace(/\.openapi\.yaml$/, '');
}

function collectTags(spec) {
  const tags = new Set();

  for (const operations of Object.values(spec.paths ?? {})) {
    if (!isObject(operations)) {
      continue;
    }

    for (const operation of Object.values(operations)) {
      if (!isObject(operation) || !Array.isArray(operation.tags)) {
        continue;
      }

      for (const tag of operation.tags) {
        if (typeof tag === 'string' && tag.length > 0) {
          tags.add(tag);
        }
      }
    }
  }

  return [...tags];
}

function inferBasePath(paths) {
  const firstPath = paths[0];

  if (!firstPath) {
    return '/';
  }

  const segments = firstPath.split('/').filter(Boolean);

  if (segments.length >= 2 && /^v\d/.test(segments[1])) {
    return `/${segments.slice(0, 2).join('/')}`;
  }

  if (segments.length > 0) {
    return `/${segments[0]}`;
  }

  return '/';
}

function inferVersion(relativeDir) {
  const segments = relativeDir.split(path.sep);
  const version = [...segments].reverse().find((segment) => /^v\d/.test(segment));

  return version ?? 'v1';
}

function humanizeServiceName(value) {
  return value
    .replace(/([a-z0-9])([A-Z])/g, '$1 $2')
    .replace(/([A-Z])([A-Z][a-z])/g, '$1 $2')
    .trim();
}

function toKebabCase(value) {
  return humanizeServiceName(value)
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/^-+|-+$/g, '');
}

function toRepoPath(filePath) {
  return path.relative(repoRoot, filePath).split(path.sep).join('/');
}

function readString(value) {
  return typeof value === 'string' && value.trim().length > 0 ? value.trim() : undefined;
}

function escapeTableCell(value) {
  return value.replace(/\|/g, '\\|');
}

function isObject(value) {
  return value !== null && typeof value === 'object' && !Array.isArray(value);
}

async function walk(root) {
  const entries = await fs.readdir(root, {withFileTypes: true});
  const files = [];

  for (const entry of entries) {
    const entryPath = path.join(root, entry.name);

    if (entry.isDirectory()) {
      files.push(...(await walk(entryPath)));
      continue;
    }

    if (entry.isFile()) {
      files.push(entryPath);
    }
  }

  return files;
}

await main();
