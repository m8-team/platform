import fs from 'node:fs/promises';
import path from 'node:path';
import {fileURLToPath} from 'node:url';

import yaml from 'js-yaml';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const docsRoot = path.resolve(__dirname, '..');
const repoRoot = path.resolve(docsRoot, '..');
const sourceOpenapiRoot = path.join(repoRoot, 'api', 'generate', 'openapi');
const sourceProtoRoot = path.join(repoRoot, 'api', 'proto');
const generatedOpenapiRoot = path.join(docsRoot, '_generated', 'openapi');
const generatedServicesRoot = path.join(generatedOpenapiRoot, 'services');
const docsOverviewTargets = [
  {
    groupSegments: ['resourcemanager'],
    title: 'Overview',
    intro: 'Этот REST reference организован по сервисам `Resource Manager`.',
    outputFile: path.join(
      docsRoot,
      'services',
      'resource-manager',
      'api-reference',
      'rest',
      'index.md',
    ),
  },
];

async function main() {
  await fs.rm(generatedOpenapiRoot, {recursive: true, force: true});
  await fs.mkdir(generatedServicesRoot, {recursive: true});

  const serviceRecords = [];
  const specFiles = (await walk(sourceOpenapiRoot))
    .filter((filePath) => filePath.endsWith('.openapi.yaml'))
    .sort();

  for (const specFile of specFiles) {
    const source = yaml.load(await fs.readFile(specFile, 'utf8'));

    if (!isObject(source) || !isObject(source.paths) || Object.keys(source.paths).length === 0) {
      continue;
    }

    const relativeDir = path.relative(sourceOpenapiRoot, path.dirname(specFile));
    const protoFile = path.join(
      sourceProtoRoot,
      relativeDir,
      path.basename(specFile).replace(/\.openapi\.yaml$/, '.proto'),
    );
    const protoMeta = await readProtoMetadata(protoFile);
    const serviceName = protoMeta.serviceName ?? inferServiceNameFromSpec(source, specFile);
    const slug = toKebabCase(serviceName);
    const displayName = humanizeServiceName(serviceName);
    const navName = toProductName(displayName);
    const description =
      protoMeta.description ??
      readString(source.info?.description) ??
      `${displayName} HTTP API.`;
    const version = readString(source.info?.version) ?? inferVersion(relativeDir);
    const tags = collectTags(source);
    const flattenTagSections = tags.length === 1 && tags[0] === serviceName;
    const groupSegments = inferServiceGroupSegments(relativeDir);

    if (flattenTagSections) {
      flattenSingleTagSections(source);
    }

    const operations = extractOperations(source, {flattenTagSections});
    const serviceDir = path.join(generatedServicesRoot, ...groupSegments, slug);

    source.info = {
      ...(isObject(source.info) ? source.info : {}),
      title: `${navName} API`,
      description,
      version,
    };

    await fs.mkdir(serviceDir, {recursive: true});
    await fs.writeFile(
      path.join(serviceDir, 'openapi.yaml'),
      yaml.dump(source, {lineWidth: -1, noRefs: true}),
      'utf8',
    );
    await fs.writeFile(
      path.join(serviceDir, 'overview.md'),
      renderServiceOverview({
        navName,
        description,
        operations,
      }),
      'utf8',
    );

    serviceRecords.push({
      groupSegments,
      slug,
      docsSlug: toKebabCase(navName),
      navName,
      description,
    });
  }

  await writeGroupOverviews(serviceRecords);
  await writeDocsOverviewPages(serviceRecords);

  console.log('Generated OpenAPI docs into docs/_generated/openapi.');
}

function renderServiceOverview({navName, description, operations}) {
  const lines = [
    '<!-- Generated by docs/scripts/sync-openapi.mjs -->',
    '',
    `# ${navName} API: REST reference`,
    '',
    description,
    '',
    '## Methods',
    '',
    '| Method | Description |',
    '| --- | --- |',
  ];

  if (operations.length === 0) {
    lines.push('| No published methods | Methods will appear here after OpenAPI generation. |');
  } else {
    for (const operation of operations) {
      lines.push(
        `| [${operation.name}](${operation.docHref}) | ${escapeTableCell(operation.description)} |`,
      );
    }
  }

  return `${lines.join('\n')}\n`;
}

async function writeGroupOverviews(serviceRecords) {
  const servicesByGroup = groupServicesByPath(serviceRecords);

  for (const [groupKey, services] of servicesByGroup.entries()) {
    const groupSegments = groupKey.length > 0 ? groupKey.split('/') : [];
    const overviewFile = path.join(generatedServicesRoot, ...groupSegments, 'overview.md');

    await fs.mkdir(path.dirname(overviewFile), {recursive: true});
    await fs.writeFile(
      overviewFile,
      renderOverviewPage({
        title: 'Overview',
        intro: 'This section contains generated OpenAPI references for the services in this group.',
        services,
        hrefForService: (service) => `./${service.slug}/overview.md`,
      }),
      'utf8',
    );
  }
}

async function writeDocsOverviewPages(serviceRecords) {
  const servicesByGroup = groupServicesByPath(serviceRecords);

  for (const target of docsOverviewTargets) {
    const groupKey = target.groupSegments.join('/');
    const services = servicesByGroup.get(groupKey) ?? [];

    await fs.mkdir(path.dirname(target.outputFile), {recursive: true});
    await fs.writeFile(
      target.outputFile,
      renderOverviewPage({
        title: target.title,
        intro: target.intro,
        services,
        hrefForService: (service) => `./${service.docsSlug}/index.md`,
      }),
      'utf8',
    );
  }
}

function renderOverviewPage({title, intro, services, hrefForService}) {
  const lines = [
    '<!-- Generated by docs/scripts/sync-openapi.mjs -->',
    '',
    `# ${title}`,
    '',
    intro,
    '',
    '## Services',
    '',
    '| Service | Description |',
    '| --- | --- |',
  ];

  if (services.length === 0) {
    lines.push('| No published services | Service references will appear here after OpenAPI generation. |');
  } else {
    for (const service of [...services].sort((left, right) => left.navName.localeCompare(right.navName))) {
      lines.push(
        `| [${service.navName}](${hrefForService(service)}) | ${escapeTableCell(service.description)} |`,
      );
    }
  }

  return `${lines.join('\n')}\n`;
}

function groupServicesByPath(serviceRecords) {
  const servicesByGroup = new Map();

  for (const service of serviceRecords) {
    const groupKey = service.groupSegments.join('/');
    const services = servicesByGroup.get(groupKey) ?? [];
    services.push(service);
    servicesByGroup.set(groupKey, services);
  }

  return servicesByGroup;
}

async function readProtoMetadata(protoFile) {
  try {
    const source = await fs.readFile(protoFile, 'utf8');
    const match = source.match(/((?:\/\/[^\n]*\n)*)service\s+([A-Za-z0-9_]+)\s*\{/m);

    if (!match) {
      return {};
    }

    const serviceBody = readBalancedBlock(source, source.indexOf('{', match.index));
    const comment = match[1]
      .split('\n')
      .map((line) => line.replace(/^\/\/\s?/, '').trim())
      .filter(Boolean)
      .join(' ')
      .trim();
    const optionDescription = readString(
      serviceBody?.match(
        /option\s+\((?:m8\.platform\.extension\.v1\.)?description\)\s*=\s*"((?:[^"\\]|\\.)*)"\s*;/m,
      )?.[1],
    );

    return {
      serviceName: match[2],
      description: optionDescription ? decodeProtoString(optionDescription) : comment || undefined,
    };
  } catch {
    return {};
  }
}

function inferServiceNameFromSpec(spec, specFile) {
  const firstTag = collectTags(spec)[0];

  if (firstTag) {
    return firstTag;
  }

  return path.basename(specFile).replace(/\.openapi\.yaml$/, '');
}

function collectTags(spec) {
  const tags = new Set();

  for (const operations of Object.values(spec.paths ?? {})) {
    if (!isObject(operations)) {
      continue;
    }

    for (const operation of Object.values(operations)) {
      if (!isObject(operation) || !Array.isArray(operation.tags)) {
        continue;
      }

      for (const tag of operation.tags) {
        if (typeof tag === 'string' && tag.length > 0) {
          tags.add(tag);
        }
      }
    }
  }

  return [...tags];
}

function extractOperations(spec, {flattenTagSections}) {
  const operations = [];

  for (const [routePath, methods] of Object.entries(spec.paths ?? {})) {
    if (!isObject(methods)) {
      continue;
    }

    for (const [method, operation] of Object.entries(methods)) {
      if (!isObject(operation)) {
        continue;
      }

      const name =
        readString(operation.summary) ??
        readString(operation.operationId) ??
        `${method.toUpperCase()} ${routePath}`;
      const fileName = readString(operation.operationId) ?? slugForFile(name);
      const description =
        readString(operation.description) ??
        readString(operation.summary) ??
        'No description.';

      operations.push({
        name,
        description,
        docHref: flattenTagSections ? `./${fileName}.md` : `./operations/${fileName}.md`,
      });
    }
  }

  return operations;
}

function flattenSingleTagSections(spec) {
  if (Array.isArray(spec.tags)) {
    delete spec.tags;
  }

  for (const methods of Object.values(spec.paths ?? {})) {
    if (!isObject(methods)) {
      continue;
    }

    for (const operation of Object.values(methods)) {
      if (!isObject(operation)) {
        continue;
      }

      delete operation.tags;
    }
  }
}

function inferVersion(relativeDir) {
  const segments = relativeDir.split(path.sep);
  const version = [...segments].reverse().find((segment) => /^v\d/.test(segment));

  return version ?? 'v1';
}

function inferServiceGroupSegments(relativeDir) {
  const segments = relativeDir.split(path.sep).filter(Boolean);
  const normalizedSegments =
    segments[0] === 'm8' && segments[1] === 'platform' ? segments.slice(2) : [...segments];

  if (normalizedSegments.length > 0 && /^v\d/.test(normalizedSegments[normalizedSegments.length - 1])) {
    normalizedSegments.pop();
  }

  return normalizedSegments;
}

function humanizeServiceName(value) {
  return value
    .replace(/([a-z0-9])([A-Z])/g, '$1 $2')
    .replace(/([A-Z])([A-Z][a-z])/g, '$1 $2')
    .trim();
}

function toKebabCase(value) {
  return humanizeServiceName(value)
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/^-+|-+$/g, '');
}

function toProductName(displayName) {
  return displayName.replace(/\s+Service$/, '').trim();
}

function slugForFile(value) {
  return value
    .replace(/[^A-Za-z0-9._-]+/g, '-')
    .replace(/^-+|-+$/g, '');
}

function readString(value) {
  return typeof value === 'string' && value.trim().length > 0 ? value.trim() : undefined;
}

function decodeProtoString(value) {
  return value
    .replace(/\\\\/g, '\\')
    .replace(/\\"/g, '"')
    .replace(/\\n/g, '\n')
    .replace(/\\t/g, '\t');
}

function escapeTableCell(value) {
  return value.replace(/\|/g, '\\|');
}

function isObject(value) {
  return value !== null && typeof value === 'object' && !Array.isArray(value);
}

function readBalancedBlock(source, openBraceIndex) {
  if (openBraceIndex < 0 || source[openBraceIndex] !== '{') {
    return undefined;
  }

  let depth = 0;

  for (let index = openBraceIndex; index < source.length; index += 1) {
    const char = source[index];

    if (char === '{') {
      depth += 1;
      continue;
    }

    if (char !== '}') {
      continue;
    }

    depth -= 1;

    if (depth === 0) {
      return source.slice(openBraceIndex + 1, index);
    }
  }

  return undefined;
}

async function walk(root) {
  const entries = await fs.readdir(root, {withFileTypes: true});
  const files = [];

  for (const entry of entries) {
    const entryPath = path.join(root, entry.name);

    if (entry.isDirectory()) {
      files.push(...(await walk(entryPath)));
      continue;
    }

    if (entry.isFile()) {
      files.push(entryPath);
    }
  }

  return files;
}

await main();
